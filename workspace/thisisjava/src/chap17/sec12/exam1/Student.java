package chap17.sec12.exam1;

public class Student {
	private String name;
	private String s;
	private int score;
	
	public Student(String name, String s, int score) {
		this.name = name;
		this.s = s;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public String getS() {
		return s;
	}

	public int getScore() {
		return score;
	}
	
	
}
