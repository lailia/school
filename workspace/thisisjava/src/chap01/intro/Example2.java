package chap01.intro;

public class Example2 {

	public static void main(String[] args) {
		String name = "김태립";
		int age = 26;
		String add = "서울시";
		String ch = "게임";
		String t = "JAVA프로그래밍";
		
		System.out.println("이름 : " + name);			//이름 : (name)
		System.out.println("나이 : " + age + "세");	//나이 : (age)세
		System.out.println("주소 : " + add);			//주소 : (add)
		System.out.println("취미 : " + ch);			//취미 : (ch)
		System.out.println("특기 : " + t);			//특기 : (t)

	}

}
